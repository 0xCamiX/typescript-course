/*

El tipado en TypeScript hace referencia a cómo declaramos una variable,
necesitamos asignar el tipo de dato, conocido como type annotation.
con esto evitamos mezclar distintos tipos de datos en una misma variable.

En JavaScript se puede declarar una variable de un tipo de valor y a lo largo
de la ejecución del programa se le puede asignar otro tipo de valor, esto
no es recomendable, ya que puede generar errores en el programa.


1. En TypeScript se declara una variable añadiendo la declaración y el tipo de dato.

Ej: const productPrice: number = 100;
*/

